document.addEventListener("DOMContentLoaded", () => {
    // ==========================
    // MOBILE MENU
    // ==========================
    const openBtn = document.getElementById("openMenu");
    const closeBtn = document.getElementById("closeMenu");
    const mobileMenu = document.getElementById("mobileMenu");

    if (openBtn && closeBtn && mobileMenu) {
        function lockScroll() {
            document.body.style.overflow = "hidden";
            document.documentElement.style.overflow = "hidden";
        }
        function unlockScroll() {
            document.body.style.overflow = "";
            document.documentElement.style.overflow = "";
        }
        function openMenu() {
            mobileMenu.classList.add("active");
            lockScroll();
        }
        function closeMenuFn() {
            mobileMenu.classList.remove("active");
            unlockScroll();
        }

        openBtn.addEventListener("click", openMenu);
        closeBtn.addEventListener("click", closeMenuFn);

        mobileMenu.querySelectorAll(".mobile-link").forEach(link => {
            link.addEventListener("click", closeMenuFn);
        });
    }

    // ==========================
    // IBAN COPY LOGIC
    // ==========================
    const ibanSpan = document.getElementById("ibanValue");
    const ibanMessage = document.getElementById("ibanMessage");
    const copyBtn = document.getElementById("copyIbanBtn");
    const copyRawBtn = document.getElementById("copyRawIbanBtn");

    if (ibanSpan && ibanMessage && copyBtn && copyRawBtn) {
        function showMessage(text) {
            ibanMessage.textContent = text;
            ibanMessage.style.opacity = "1";
            setTimeout(() => {
                ibanMessage.style.opacity = "0";
            }, 2000);
        }

        function copyToClipboard(text) {
            if (navigator.clipboard && window.isSecureContext) {
                // API modern (https)
                return navigator.clipboard.writeText(text);
            } else {
                // fallback vechi
                const textarea = document.createElement("textarea");
                textarea.value = text;
                textarea.style.position = "fixed";
                textarea.style.left = "-9999px";
                document.body.appendChild(textarea);
                textarea.focus();
                textarea.select();
                try {
                    document.execCommand("copy");
                } catch (e) {
                    console.error("Copy failed", e);
                }
                document.body.removeChild(textarea);
                return Promise.resolve();
            }
        }

        copyBtn.addEventListener("click", () => {
            const iban = ibanSpan.textContent.trim();
            copyToClipboard(iban).then(() => {
                showMessage("✓ IBAN copied!");
            });
        });

        copyRawBtn.addEventListener("click", () => {
            const ibanRaw = ibanSpan.textContent.replace(/\s+/g, "");
            copyToClipboard(ibanRaw).then(() => {
                showMessage("✓ Raw IBAN copied!");
            });
        });
    }
});
